#
# used to take raw hex without the '\x' designation from byte stream
# 
hexit() { xargs echo $1 | sed -r "s/([0-9A-Fa-f]{2})/\\\x\1/g"; }
#
## Example usage:
#
# parseida $test.infile | hexit > test.outfile
#
## Example output:
# 
# \x99\x99\x99\x99\x99\x99\x99\x99\x99\x99\x99\x99\x99\x99\xAA\xAA\xAA\xAA\xAA\xAA\xAA\xAA\xAA\xAA\xAA\xAA\xBB\xBB\xBB\xBB\xBB\xBB\xBB\xBB\xCC\xCC\xCC\xCC\xCC\xCC\xCD\xDD\xDD\xDD\xDD
# 
